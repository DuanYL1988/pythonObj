#=======================================================================
# 1.定义类名后要接():
# 2.函数定义使用def
# 3.参数可以设置默认值,在调用时候可以不写参数
#=======================================================================
class CommonUtil():

  def getSum(numberArr=[1,2,3]):
    return sum(numberArr)


print(CommonUtil.getSum([1,2,3,4]))
print(CommonUtil.getSum())
